Number1 = int(input("Please enter the number 1 : "))

Number2 = int(input("Please enter the number 2 : "))

choice = input("Please enter the choice : ")

if(choice is '+'):
    print(Number1+Number2)

elif(choice == '-'):
    print(Number1-Number2)

elif(choice == '*'):
    print(Number1*Number2)

elif(choice == '/'):
    print(Number1/Number2)
